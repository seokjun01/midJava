package lang.string;

public class CharArrayMain {
    public static void main(String[] args) {
        char[] charArr = new char[]{'h', 'e', 'l', 'l', 'o'};

        System.out.println(charArr);

        String str = "hello";
        System.out.println(str);

        //char[] 을 써서 문자열을 쓰는 것보다는, String 을 사용하는 것이 훨씬 편함
    }
}
